package com.rick.mall.rimalljpa.entity;

import javax.persistence.*;
import java.math.BigDecimal;
import java.util.Objects;

/**
 * @Author zhuhaotian
 * @Date 2023/7/14
 */

@Entity
@Table(name = "pms_product_full_reduction", schema = "ri-mall")
public class PmsProductFullReduction {
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Id
    @Column(name = "id")
    private long id;
    @Basic
    @Column(name = "product_id")
    private Long productId;
    @Basic
    @Column(name = "full_price")
    private BigDecimal fullPrice;
    @Basic
    @Column(name = "reduce_price")
    private BigDecimal reducePrice;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public Long getProductId() {
        return productId;
    }

    public void setProductId(Long productId) {
        this.productId = productId;
    }

    public BigDecimal getFullPrice() {
        return fullPrice;
    }

    public void setFullPrice(BigDecimal fullPrice) {
        this.fullPrice = fullPrice;
    }

    public BigDecimal getReducePrice() {
        return reducePrice;
    }

    public void setReducePrice(BigDecimal reducePrice) {
        this.reducePrice = reducePrice;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        PmsProductFullReduction that = (PmsProductFullReduction) o;
        return id == that.id && Objects.equals(productId, that.productId) && Objects.equals(fullPrice, that.fullPrice) && Objects.equals(reducePrice, that.reducePrice);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, productId, fullPrice, reducePrice);
    }
}
